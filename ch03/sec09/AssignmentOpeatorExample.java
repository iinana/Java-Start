package ch03.sec09;

public class AssignmentOpeatorExample {
    public static void main (String[] args) {
        int res = 0;
        res += 10;
        System.out.println("result = " + res);

        res -= 5;
        System.out.println("result = " + res);

        res *= 3;
        System.out.println("result = " + res);

        res /= 5;
        System.out.println("result = " + res);

        res %= 3;
        System.out.println("result = " + res);
    }
}
